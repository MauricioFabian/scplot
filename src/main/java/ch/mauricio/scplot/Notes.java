package ch.mauricio.scplot;

public class Notes {

	
	private int year;
	private double note;
	public void setYear(int year) {
		this.year = year;
	}
	public void setNote(double note) {
		this.note = note;
	}
	public int getYear() {
		return year;
	}
	public double getNote() {
		return note;
	}
	
	
}
